def send_cmd(name, label, data, repeats):
    if isinstance(data, CompositeCmd):
        try:
            while True:
                for sub_name, sub_label, sub_data in data.commands:
                    iq8 = generate_iq8(sub_data, repeats=data.sub_repeats)
                    transmit(iq8)
                    if data.gap > 0:
                        time.sleep(data.gap)
                if repeats != 0:
                    break
                r, _, _ = select.select([sys.stdin], [], [], 0.15)
                if r:
                    sys.stdin.readline()
                    break
        except KeyboardInterrupt:
            print("\n  Stopped.")
        return

    if repeats == 0:
        print(f"Sending '{label}' in hold mode. Repeating until Ctrl+C...")
        try:
            while True:
                iq8 = generate_iq8(data, repeats=3)
                transmit(iq8)
                r, _, _ = select.select([sys.stdin], [], [], 0.1)
                if r:
                    sys.stdin.readline()
                    break
        except KeyboardInterrupt:
            print("\n  Stopped.")
    else:
        print(f"Sending '{label}' x{repeats}...")
        iq8 = generate_iq8(data, repeats=repeats)
        transmit(iq8)
        print("Done.")


