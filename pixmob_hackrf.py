# ====================== TRANSMISSION ======================
def transmit(iq8_data, freq=None, gain=None, amp=None, sample_rate=SAMPLE_RATE):
    """Transmit IQ8 data using hackrf_transfer."""
    f = freq if freq is not None else current_freq
    g = gain if gain is not None else current_gain
    a = amp if amp is not None else amp_enabled

    with tempfile.NamedTemporaryFile(suffix='.iq8', delete=False) as fh:
        fh.write(iq8_data)
        tmp_path = fh.name

    try:
        cmd = [
            'hackrf_transfer',
            '-t', tmp_path,
            '-f', str(int(f)),
            '-s', str(int(sample_rate)),
            '-a', '1' if a else '0',
            '-x', str(g),
        ]
        print(f"TX: freq={f/1e6:.3f} MHz  gain={g}  amp={'ON' if a else 'OFF'}")
        result = subprocess.run(cmd, capture_output=True, text=True)
        if result.returncode != 0:
            print(f"Error: {result.stderr}")
            return False
        return True
    finally:
        os.unlink(tmp_path)


