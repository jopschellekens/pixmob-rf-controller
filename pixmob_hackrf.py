def fine_sweep():
    """Fine-grain TX sweep around 868 MHz.
    Transmits 'nothing' (wake) at tight frequency steps.
    Listen for the bracelet's audible response (high-pitched squeal)
    to find the most responsive frequency.
    """
    print("\n" + "="*60)
    print("  FINE TX SWEEP AROUND 868 MHz")
    print("  Hold bracelet near the HackRF antenna.")
    print("  Listen for the high-pitched squeal from the bracelet.")
    print("  Note which frequency gives the strongest response.")
    print("="*60)

    center = 868.0
    step = 0.025  # 25 kHz steps
    half_range = 0.2  # +/- 200 kHz

    start_mhz = center - half_range
    end_mhz = center + half_range
    steps = int((end_mhz - start_mhz) / step) + 1

    # Use "nothing" command since it causes the audible response
    name, label, data = COMMANDS[0]
    # Short burst
    iq8 = generate_iq8(data, repeats=3)

    input(f"\n  Press Enter to sweep {start_mhz:.3f} - {end_mhz:.3f} MHz in {step:.0f} kHz steps...\n")

    for i in range(steps):
        freq_mhz = start_mhz + i * step
        freq_hz = freq_mhz * 1e6
        print(f"  {freq_mhz:.3f} MHz  ", end="", flush=True)
        transmit(iq8, freq=freq_hz, gain=current_gain, amp=amp_enabled)
        time.sleep(0.25)

    print("\nFine sweep complete.")


def rx_scan():
    """Use hackrf_sweep to scan for signals around 868 MHz.
    Helps find LO leakage from the bracelet's receiver.

    Hold the bracelet very close to the HackRF antenna (almost touching).
    The LO leakage signal is very weak.
    """
    print("\n" + "="*60)
    print("  RX SPECTRUM SCAN (look for bracelet LO leakage)")
    print("  Hold the bracelet VERY CLOSE to the HackRF antenna.")
    print("  The HackRF will listen around 868 MHz for any signals.")
    print("  First scan: bracelet AWAY from antenna (baseline)")
    print("  Second scan: bracelet NEAR antenna")
    print("  Difference may reveal the bracelet's local oscillator.")
    print("="*60)

    center = 868.0
    span = 4.0  # MHz
    low = center - span / 2
    high = center + span / 2

    import datetime

    for scan_name in ["BASELINE (bracelet away)", "NEAR (bracelet close)"]:
        input(f"\n  {scan_name} — Press Enter to start 3-second scan...")
        # hackrf_sweep outputs lines like: 2025-01-01 12:00:00, 868.000000, 869.000000, -45.2, -44.1, ...
        ts = datetime.datetime.now().strftime("%Y%m%d_%H%M%S")
        outfile = f"/tmp/pixmob_sweep_{ts}.csv"
        try:
            cmd = [
                'hackrf_sweep',
                '-f', f"{low}:{high}",
                '-w', '500000',  # 500 kHz per bin
                '-n', '1',       # single sweep
                '-l', '32',      # LNA gain
                '-o', outfile,
            ]
            print(f"  Running: {' '.join(cmd)}")
            # Run with timeout so it doesn't hang
            result = subprocess.run(cmd, timeout=10, capture_output=True, text=True)
            if os.path.exists(outfile):
                with open(outfile) as f:
                    data = f.read()
                # Parse CSV: format is
                # date time, freq_low, freq_high, db_value_1, db_value_2, ...
                lines = data.strip().split('\n')
                if lines:
                    print(f"  Got {len(lines)} sweep lines")
                    # Find strongest signals (minimum dB value since it's negative)
                    best = None
                    best_db = -999
                    for line in lines:
                        if line.startswith('#') or not line.strip():
                            continue
                        parts = line.split(',')
                        if len(parts) >= 5:
                            try:
                                f_low = float(parts[1].strip())
                                f_high = float(parts[2].strip())
                                db_vals = [float(p.strip()) for p in parts[3:] if p.strip()]
                                if db_vals:
                                    min_db = min(db_vals)
                                    if min_db > best_db:
                                        best_db = min_db
                                        # Find which bin has the strongest signal
                                        strongest_idx = db_vals.index(min_db)
                                        bin_width = (f_high - f_low) / len(db_vals)
                                        best_freq = f_low + strongest_idx * bin_width + bin_width / 2
                                        best = (best_freq, min_db)
                            except (ValueError, IndexError):
                                continue
                    if best:
                        print(f"  Strongest signal: {best[0]:.4f} MHz at {best[1]:.1f} dB")
                    else:
                        print(f"  No signals detected above noise floor")
                else:
                    print("  No sweep data received")
                os.unlink(outfile)
            else:
                print(f"  No output file created")
                if result.stderr:
                    print(f"  stderr: {result.stderr.strip()}")
        except subprocess.TimeoutExpired:
            print("  Sweep timed out")
        except FileNotFoundError:
            print("  'hackrf_sweep' not found. Install hackrf-tools.")
            break

    print("\nRX scan complete.")


