# ====================== IQ8 FILE GENERATION ======================
def raw_to_iq8(raw_data, sample_rate=SAMPLE_RATE):
    """Convert Flipper RAW_Data to HackRF IQ8 signed 8-bit format.
    
    OOK modulation: Carrier ON = I=127, Q=0. Carrier OFF = I=0, Q=0.
    Returns bytes object.
    """
    samples = []
    samples_per_us = sample_rate / 1e6

    for val in raw_data:
        duration_us = abs(val)
        num_samples = int(round(duration_us * samples_per_us))
        if val > 0:
            # Carrier ON: I=127, Q=0
            samples.extend([127, 0] * num_samples)
        else:
            # Carrier OFF: I=0, Q=0
            samples.extend([0, 0] * num_samples)

    return bytes(int(s) & 0xFF for s in samples)


def generate_iq8(raw_data, repeats=1, gap_us=4500, sample_rate=SAMPLE_RATE):
    """Generate IQ8 data for multiple repeats with gap between repeats."""
    single = raw_to_iq8(raw_data, sample_rate)
    gap_samples = int(round(gap_us * sample_rate / 1e6))
    gap = bytes([0, 0] * gap_samples)
    return (single + gap) * repeats


def save_iq8(data, filepath):
    """Save IQ8 data to file."""
    with open(filepath, 'wb') as f:
        f.write(data)


